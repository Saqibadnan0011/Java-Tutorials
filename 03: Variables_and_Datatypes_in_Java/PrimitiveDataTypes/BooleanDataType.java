public class BooleanDataType {
    public static void main(String[] args) {
        int n1 = 25;
        int n2 = 18;

        // declaring true or false
        boolean b1 = true;
        boolean b2 = false;

        if (n1 > n2) {
            System.out.println(b1);
        } else {
            System.out.println(b2);
        }
    }
}
