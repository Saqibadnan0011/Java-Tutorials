public class DataTypes {
    public static void main(String[] args) {
        // Primitive Datatype

        // Boolean
        boolean result = true;
        // Character
        char capitalC = 'C';
        // Byte
        byte b = 100;
        // Short
        short s = 10000;
        // Integer
        int i = 1000000;
        // Double
        double d2 = 1.234e2;
        // Double
        double d1 = 123.4;
        // Float
        float f1 = 123.4f;
        // Long
        long l = 122343334444L;
    }
}
